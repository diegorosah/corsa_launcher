@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SettingsScreen(
    viewModel: SettingsViewModel,
    onBackClick: () -> Unit
) {
    val preferredApp by viewModel.preferredNavApp.collectAsState()

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Settings") },
                navigationIcon = {
                    IconButton(onClick = onBackClick) {
                        Icon(Icons.Default.ArrowBack, contentDescription = "Back")
                    }
                }
            )
        }
    ) { innerPadding ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(innerPadding)
                .padding(16.dp)
        ) {
            Text(
                text = "Navigation App",
                style = MaterialTheme.typography.titleLarge,
                color = MaterialTheme.colorScheme.primary
            )
            Spacer(modifier = Modifier.height(16.dp))
            
            NavAppOption(
                name = "Waze",
                packageName = "com.waze",
                isSelected = preferredApp == "com.waze",
                onSelect = { viewModel.setPreferredNavApp("com.waze") }
            )
            
            NavAppOption(
                name = "Google Maps",
                packageName = "com.google.android.apps.maps",
                isSelected = preferredApp == "com.google.android.apps.maps",
                onSelect = { viewModel.setPreferredNavApp("com.google.android.apps.maps") }
            )
        }
    }
}

@Composable
fun NavAppOption(
    name: String,
    packageName: String,
    isSelected: Boolean,
    onSelect: () -> Unit
) {
    Row(
        modifier = Modifier
            .fillMaxWidth()
            .clickable(onClick = onSelect)
            .padding(vertical = 12.dp),
        verticalAlignment = Alignment.CenterVertically
    ) {
        RadioButton(
            selected = isSelected,
            onClick = onSelect
        )
        Spacer(modifier = Modifier.width(16.dp))
        Column {
            Text(text = name, style = MaterialTheme.typography.bodyLarge)
            Text(text = packageName, style = MaterialTheme.typography.labelSmall)
        }
    }
}
